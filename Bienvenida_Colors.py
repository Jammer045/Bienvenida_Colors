color = input("Por favor, ingrese un color: ").lower()

if color == "blanco" or color == "negro":
    print("Te falta diseño")
elif color == "verde":
    print("Buenos días señor Hulk")
elif color == "azul":
    print("Buenos días señor Aquaman")
elif color == "amarillo":
    print("Buenos días señor Canario")
elif color == "rojo":
    print("Buenos días morenazo de fuego")
elif color == "marron":
    print("Bienvenido Mexicano")
else:
    print("Excelente elección señor, lo añadiremos al catálogo")